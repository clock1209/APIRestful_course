    /**
     * Handle the product "updated" event.
     *
     * @param  \App\Product  $product
     * @return void
     */
    public function updated(Product $product)
    {
        if ($product->quantity == 0 and $product->isAvailable()) {
            $product->status = Product::PRODUCT_NOT_AVAILABLE;
            $product->save();
        }
    }

